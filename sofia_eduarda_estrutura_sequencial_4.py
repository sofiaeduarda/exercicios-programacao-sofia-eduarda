nota1 = float(input("Insira um número: "))
nota2 = float(input("Insira outro número: "))
nota3 = float(input("Insira outro número: "))
nota4 = float(input("Insira outro número: "))
media = (nota1 + nota2 + nota3 + nota4) / 4
print(f"A sua média é = {media}")